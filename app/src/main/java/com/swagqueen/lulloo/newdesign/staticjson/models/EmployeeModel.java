package com.swagqueen.lulloo.newdesign.staticjson.models;

public class EmployeeModel {

    private String empId;
    private String empName;
    private String empDesignation;
    private String empSalary;
    private String empFatherNamer;


    public String getEmpId() {
        return empId;
    }

    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getEmpDesignation() {
        return empDesignation;
    }

    public void setEmpDesignation(String empDesignation) {
        this.empDesignation = empDesignation;
    }

    public String getEmpSalary() {
        return empSalary;
    }

    public void setEmpSalary(String empSalary) {
        this.empSalary = empSalary;
    }

    public String getEmpFatherNamer() {
        return empFatherNamer;
    }

    public void setEmpFatherNamer(String empFatherNamer) {
        this.empFatherNamer = empFatherNamer;
    }
}
